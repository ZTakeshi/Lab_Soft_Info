    public void Table(){
        
        DefaultTableModel model = (DefaultTableModel) jTClientes.getModel();
        model.setNumRows(0);
        AC_DAOCLIENTE cdao  = new AC_DAOCLIENTE();
        
        for (AC_MDCLIENTE c: cdao.Mostrar()){
            
            model.addRow(new Object[]{
            
                c.getIdcliente(),
                c.getNome(),
                c.getEmail(),
                c.getCpf(),
                c.getTelefone(),
                c.getRg(),
                c.getCidade(),
                c.getEstado(),
                c.getCnh()
                
            });
            
        }
        
    }
    
    public void BuscarCliente(String nome){
        
        DefaultTableModel model = (DefaultTableModel) jTClientes.getModel();
        model.setNumRows(0);
        AC_DAOCLIENTE cdao  = new AC_DAOCLIENTE();
        
        for (AC_MDCLIENTE c: cdao.BuscarCliente(nome)){
            
            model.addRow(new Object[]{
           
                c.getIdcliente(),
                c.getNome(),
                c.getEmail(),
                c.getCpf(),
                c.getTelefone(),
                c.getRg(),
                c.getCidade(),
                c.getEstado(),
                c.getCnh()
                
            });
            
        }
        
    }
    
