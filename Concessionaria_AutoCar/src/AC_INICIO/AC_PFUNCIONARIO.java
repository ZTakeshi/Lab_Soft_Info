    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        
        if(jTClientes.getSelectedRow() != 1){
         
        Connection con = ConexaoAC.OpenConect();
        AC_MDCLIENTE c = new AC_MDCLIENTE();
        AC_DAOCLIENTE dao = new AC_DAOCLIENTE();
        AC_PFUNCIONARIO table = new AC_PFUNCIONARIO();

        c.setNome(alt_nome.getText());
        c.setEmail(alt_email.getText());
        c.setCpf(alt_cpf.getText());
        c.setTelefone(alt_fone.getText());
        c.setRg(alt_rg.getText());
        c.setCidade(alt_cidade.getText());
        c.setEstado(alt_estado.getText());
        c.setCnh(alt_cnh.getText());
        c.setIdcliente((int)jTClientes.getValueAt(jTClientes.getSelectedRow(), 0));
        
        dao.AlterarCliente(c);
        
        alt_nome.setText("");
        alt_email.setText("");
        alt_cpf.setText("");
        alt_fone.setText("");
        alt_rg.setText("");
        alt_cidade.setText("");
        alt_estado.setText("");
        alt_cnh.setText("");
        
        
        Table();
        
        }else{
            
            JOptionPane.showMessageDialog(null, "Selecione uma ve√≠culo para fazer a altera√ß√£o");
            
        }
        
    }//GEN-LAST:event_jButton2ActionPerformed

