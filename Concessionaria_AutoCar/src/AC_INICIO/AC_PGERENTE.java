    private void btn_altActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_altActionPerformed
        
        if(jTVeiculos.getSelectedRow() != 1){
         
        Connection con = ConexaoAC.OpenConect();
        AC_MDVEICULOS v = new AC_MDVEICULOS();
        AC_DAOVEICULOS dao = new AC_DAOVEICULOS();
        AC_PGERENTE table = new AC_PGERENTE();

        v.setModelo(alt_modelo.getText());
        v.setFabricante(alt_fabricante.getText());
        v.setCor(alt_cor.getText());
        v.setAno(Integer.parseInt(alt_ano.getText()));
        v.setPreco(Double.parseDouble(alt_preco.getText()));
        v.setChassi(alt_chassi.getText());
        v.setIdveiculos((int)jTVeiculos.getValueAt(jTVeiculos.getSelectedRow(), 0));
        
        dao.Alterar(v);
        
        alt_modelo.setText("");
        alt_fabricante.setText("");
        alt_cor.setText("");
        alt_ano.setText("");
        alt_preco.setText("");
        alt_chassi.setText("");
        
        
        Table();
        
        }else{
            
            JOptionPane.showMessageDialog(null, "Selecione uma veículo para fazer a alteração");
            
        }
                
    }//GEN-LAST:event_btn_altActionPerformed

