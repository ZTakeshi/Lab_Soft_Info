    public void Table(){
        
        DefaultTableModel model = (DefaultTableModel) jTVeiculos.getModel();
        model.setNumRows(0);
        AC_DAOVEICULOS vdao  = new AC_DAOVEICULOS();
        
        for (AC_MDVEICULOS v: vdao.Listar()){
            
            model.addRow(new Object[]{
            
                v.getIdveiculos(),
                v.getModelo(),
                v.getFabricante(),
                v.getCor(),
                v.getAno(),
                v.getPreco(),
                v.getChassi()
                
            });
            
        }
        
    }

    public void BuscarModel(String mode){
        
        DefaultTableModel model = (DefaultTableModel) jTVeiculos.getModel();
        model.setNumRows(0);
        AC_DAOVEICULOS vdao  = new AC_DAOVEICULOS();
        
        for (AC_MDVEICULOS v: vdao.BuscarModelo(mode)){
            
            model.addRow(new Object[]{
           
                v.getIdveiculos(),
                v.getModelo(),
                v.getFabricante(),
                v.getCor(),
                v.getAno(),
                v.getPreco(),
                v.getChassi()
                
            });
            
        }
        
    }
   
    
    
